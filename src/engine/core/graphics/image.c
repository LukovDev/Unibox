// Загрузить картинку:
Image* Image_load(const char *filepath, int format) {
    Image *image = mm_alloc(sizeof(Image));
    if (!image) mm_alloc_error();
    if (!format) format = IMG_RGBA;
    if (filepath == NULL) return NULL;
    image->data = stbi_load(filepath, &image->width, &image->height, &image->channels, format);
    if (image->data == NULL) {
        mm_free(image);
        return NULL;
    }
    image->from_stbi = true;
    mm_used_size_add(Image_get_size(image));
    return image;
}


// Сохранить картинку:
bool Image_save(Image *image, const char *filepath, const char *format) {
    if (!image || !image->data || !filepath || !format) return false;
    bool success = false;

    // Определяем формат по строке:
    if (strcmp(format, "png") == 0) {
        success = stbi_write_png(
            filepath, image->width, image->height,
            image->channels, image->data,
            image->width * image->channels);
    } else if (strcmp(format, "jpg") == 0 || strcmp(format, "jpeg") == 0) {
        success = stbi_write_jpg(
            filepath, image->width, image->height, image->channels, image->data, 100); // Качество 100%.
    } else if (strcmp(format, "bmp") == 0) {
        success = stbi_write_bmp(filepath, image->width, image->height, image->channels, image->data);
    } else if (strcmp(format, "tga") == 0) {
        success = stbi_write_tga(filepath, image->width, image->height, image->channels, image->data);
    } else {
        fprintf(stderr, "Image_save: Unknown format: \"%s\"\n", format);
        return false;
    }

    if (!success) {
        fprintf(stderr, "Image_save: Saving image failed: \"%s\"\n", filepath);
        return false;
    }
    return true;
}


// Копировать картинку в памяти:
Image* Image_copy(const Image *source) {
    if (!source) return NULL;

    Image* copy = mm_alloc(sizeof(Image));
    if (!copy) mm_alloc_error();

    // Копируем простые поля:
    copy->width = source->width;
    copy->height = source->height;
    copy->channels = source->channels;
    copy->from_stbi = false;

    // Вычисляем размер буфера:
    size_t size = (size_t)source->width * source->height * source->channels;

    if (source->data && size > 0) {
        copy->data = mm_alloc(size);
        if (!copy->data) {
            free(copy);
            mm_alloc_error();
        }
        memcpy(copy->data, source->data, size);
    } else { copy->data = NULL; }

    return copy;
}


// Создать стандартную картинку:
Image* Image_create_default() {
    if (Image_default_icon_size == 0) return NULL;

    Image *image = mm_alloc(sizeof(Image));
    if (!image) mm_alloc_error();

    unsigned char* buffer = mm_alloc(Image_default_icon_size);
    if (!buffer) mm_alloc_error();

    // Копируем и используем стандартную картинку:
    memcpy(buffer, Image_default_icon, Image_default_icon_size);
    image->data = buffer;
    image->width = Image_default_icon_width;
    image->height = Image_default_icon_height;
    image->channels = IMG_RGBA;
    image->from_stbi = false;

    return image;
}


// Освободить память картинки:
void Image_destroy(Image **image) {
    if (!image || !*image) return;
    if ((*image)->data != NULL) {
        if ((*image)->from_stbi) {
            stbi_image_free((*image)->data);
            mm_used_size_sub(Image_get_size(*image));
        } else { mm_free((*image)->data); }
    }
    mm_free(*image);
    *image = NULL;
}


// Получить размер картинки в байтах:
size_t Image_get_size(Image *image) {
    if (!image) return 0;
    return image->width * image->height * image->channels;
}


